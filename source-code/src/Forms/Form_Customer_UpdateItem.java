    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String id = txtCustomerID.getText().trim();
        String name = txtCustomerName.getText();
        String gender = rdbGender_Male.isSelected() ? "Nam" : "Nữ";
        String dobString = txtCustomerDOB.getText();
        java.sql.Date dob = convertStringToDate(dobString);
        String address = txtCustomerAddress.getText();
        String phone = txtCustomerPhone.getText();
        String email = txtCustomerEmail.getText();

        if (id.isEmpty() || name.isEmpty() || dob == null || address.isEmpty() || phone.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin khách hàng.");
            return;
        }

        Customer customer = new Customer(id, name, gender, dob, address, phone, email);
        if (customerRepo.updateCustomer(customer)) {
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin khách hàng thành công!");
            if (dataChangedListener != null) {
                dataChangedListener.onDataChanged();
            }
            dispose(); 
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin khách hàng thất bại!");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

