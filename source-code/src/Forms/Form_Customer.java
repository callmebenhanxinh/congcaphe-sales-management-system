    public void loadCustomerToTable() {
        DefaultTableModel model = (DefaultTableModel) gvCustomers.getModel();
        model.setRowCount(0);
        
        List<Models.Customer> customers = customerRepo.getAllCustomers();

        for (Models.Customer c : customers) {
            model.addRow(new Object[]{
                c.getID(), 
                c.getName(), 
                c.getGender(),
                c.getDOB(),
                c.getAddress(),
                c.getPhone(),
                c.getEmail()
            });
        }
    }

