    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = gvCustomers.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một khách hàng để xóa.");
            return;
        }

        // Lấy ID của khách hàng từ cột đầu tiên của hàng được chọn
        String customerIDToDelete = (String) gvCustomers.getValueAt(selectedRow, 0);

        int confirmResult = JOptionPane.showConfirmDialog(this,
                "Bạn có chắc chắn muốn xóa khách hàng có mã khách hàng: " + customerIDToDelete.trim() + "?",
                "Xác nhận xóa",
                JOptionPane.YES_NO_OPTION);

        if (confirmResult == JOptionPane.YES_OPTION) {
            if (customerRepo.deleteCustomer(customerIDToDelete)) {
                JOptionPane.showMessageDialog(this, "Xóa khách hàng thành công!");
                loadCustomerToTable();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa khách hàng thất bại.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

