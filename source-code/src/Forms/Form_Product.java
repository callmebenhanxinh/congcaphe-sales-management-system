    private java.sql.Date convertStringToDate(String dateString) {
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd"); 
        try {
            java.util.Date parsed = format.parse(dateString);
            return new java.sql.Date(parsed.getTime()); 
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Định dạng ngày không hợp lệ. Vui lòng nhập theo yyyy-MM-dd.");
            return null;
        }
    }
    
    public void loadProductToTable() {
        DefaultTableModel model = (DefaultTableModel) tableProducts.getModel();
        model.setRowCount(0);
        
        List<Product> products = productRepo.getAllProducts();

        for (Product p : products) {
            model.addRow(new Object[]{
                p.getProductID(), 
                p.getProductName(),
                p.getProductQuantity(),
                p.getProductPrice(),
                p.getProductImage()
            });
        }
    }
    
    private void openFile(String filePath) {
        try {
            File file = new File(filePath);
            if (Desktop.isDesktopSupported()) {
                Desktop.getDesktop().open(file);
            } else {
                JOptionPane.showMessageDialog(this, "Không thể mở file.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi mở file: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
