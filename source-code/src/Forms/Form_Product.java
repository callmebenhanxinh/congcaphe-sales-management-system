    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableProducts.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một món ăn trong thực đơn để xóa.");
            return;
        }

        String productIDToDelete = (String) tableProducts.getValueAt(selectedRow, 0);

        int confirmResult = JOptionPane.showConfirmDialog(this,
            "Bạn có chắc chắn muốn xóa món ăn có mã món ăn: " + productIDToDelete.trim() + "?",
            "Xác nhận xóa",
            JOptionPane.YES_NO_OPTION);

        if (confirmResult == JOptionPane.YES_OPTION) {
            if (productRepo.deleteProduct(productIDToDelete)) {
                JOptionPane.showMessageDialog(this, "Xóa món ăn khỏi thực đơn thành công!");
                loadProductToTable();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa món ăn khỏi thực đơn thất bại.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

